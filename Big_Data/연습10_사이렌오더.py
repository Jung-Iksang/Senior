# 연습 10: 사이렌오더를 통해 음료를 미리 예약하는 프로그램
# 메뉴: 아메리카노 3000원, 카페라떼 4000원, 바닐라라떼 4500원

def price(menue):
    """메뉴번호를 입력받아 해당 메뉴와 가격을 출력하는 함수"""
    if menue == 1:
        print("아메리카노: 3,000원")
    elif menue == 2:
        print("카페라떼: 4,000원")
    elif menue == 3:
        print("바닐라라떼: 4,500원")
    else:
        print("잘못된 메뉴 선택입니다.")

# 메뉴 선택 및 함수 호출
menue = int(input("메뉴선택(1:아메리카노/2:카페라떼/3:바닐라라떼) "))
price(menue)